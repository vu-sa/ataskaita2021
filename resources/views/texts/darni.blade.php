<h1 style="text-align: center;"><strong>Programos, klubai, projektai</strong></h1>
<p>
Ataskaitiniu laikotarpiu didžiausias dėmesys buvo skiriamas PKP bendradarbiavimui tarpusavyje 
bei su kitais VU ir VU SA dariniais. Buvo tęsiami PKP vadovų susitikimai ir informacijos 
sklaida, dalijimasis gerąja patirtimi ir veiklos rezultatais tarp PKP vadovų. 
PKP nariai buvo kviečiami dalyvauti VU SA organizuojamuose vasaros, informacinės, 
marketingo ir organizacinės sričių mokymuose. Rudenį vykdyti PKP mokymai, 
kurių metu visi nariai turėjo galimybę geriau susipažinti su VU SA, naujai 
apsibrėžti savo veiklos kryptis ir įgyti naujų kompetencijų komunikacijos 
srityje. Didžioji dalis PKP prisistatė rugsėjį vykusioje VU Karjeros dienų 
kontaktų mugėje. Didelis dėmesys skirtas PKP instituciniam stiprinimui – 
vykdyti keturių PKP metų veiklos planavimai, nuolat vykdytos konsultacijos 
geriausiems situacijų sprendimo būdams rasti bei sklandžiai veiklai vykdyti. 
Taip pat per šiuos metus universitete įsikūrė du nauji klubai – <i>VU Fotoklubas</i> ir <i>VU Antroklubas</i>, 
kurie jau pradėjo savo veiklą pritraukdami didelį kiekį studentų. 
</p>


<h3 style="text-align: left;"><i><strong>Be etikečių </strong></i></h3>
<p><i>Ataskaitiniu laikotarpiu programos veiklą koordinavo Ieva Budreikaitė. </i></p>
<p>
<i>Be etikečių </i>– nuo 2010-ųjų VU SA vykdoma programa, 
aktyviai veikianti socialinės atskirties mažinimo ir žmogaus teisių srityse. 
Šia programa siekiama skatinti pagarbą VU bendruomenėje, 
laužyti nusistovėjusius stereotipus ir didinti Universiteto atvirumą 
skatinant visapusišką socialiai pažeidžiamų grupių integraciją. 
Pagrindinės programos vertybės: lygybė, atvirumas ir 
pagarba kiekvieno žmogaus individualumui. 
</p>
<p>
Programos darbas šiais metais buvo orientuotas į turinio socialinėms medijoms 
kūrimą –veiklai persikėlus į nuotolį, siekta įgyvendinti savo misiją 
dalijantis aktualijomis, rašant straipsnius socialinės, lyčių lygybės, 
emocinės sveikatos ir kitomis aktualiomis temomis. Kiekvieną sekmadienį 
dalintasi menininkų, susijusių su kultūra, socialiai pažeidžiamomis grupėmis 
ar emocine sveika, istorijomis ir darbais. Siekiant įrašų atpažinimo taikyta 
grotažymė <i>#kultūravisų</i>. Taip pat, ataskaitiniu laikotarpiu organizuota paskaita 
apie visavertiškumo jausmą ir meditaciją. Šių metų Hallophobia renginio metu vesta 
paskaita apie studentų patiriamas emocines krizes. Minint savaitę prieš rasizmą 
kartu su VU SA ESN rengta paskaita apie rasizmą ir ksenofobiją Lietuvoje, 
kurioje teisinį ir realybės žmogaus teisių aspektus pristatė lektorės iš Žmogaus 
teisių balso ir Lietuvos žmogaus teisių centro. Tolerancijos dienos proga organizuota 
diskusija, kurioje renginio dalyviai buvo kviečiami diskutuoti, kaip tam tikros 
socialinės situacijos veikia žmogaus psichologinę sveikatą. Spalio mėnesį, 
bendradarbiaujant su Universiteto LGBT+ grupe, organizuotas mados pristatymo 
renginys Atverk savo spintą, į kurį kviesti keli stiliumi besidomintys LGBT+ 
bendruomenės asmenys. 
</p>
