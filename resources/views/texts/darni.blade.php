<h3 style="text-align: left;"><i><strong>Erasmus Student Network Vilnius University (ESN VU) </strong></i></h3>
<p><i>Ataskaitiniu laikotarpiu programos veiklą koordinavo Viktorija Milko.</i></p>
<p>
<i>ESN Vilnius University </i>– VU SA programa, skatinanti studentų mobilumą bei padedanti užsienio studentams sėkmingai integruotis Vilniaus universitete. Pagrindinis ESN Vilnius University tikslas – padėti į VU atvykstantiems užsienio studentams prisitaikyti naujoje aplinkoje, supažindinti juos su studijų tvarka, mūsų šalies kultūra bei tradicijomis.  
</p>
<p>
<i>ESN Vilnius University </i> veikla buvo orientuota į įvairių renginių, skirtų 
užsienio studentų integracijos didinimui ir bendruomeniškumo skatinimui, organizavimą. 
Kaip ir kiekvieną semestrą veikla tradiciškai pradėta su <i>Orientation Week</i>, kurios 
metu stengtasi supažindinti užsienio studentus tarpusavyje bei suteikti visą 
būtinąją informaciją apie Universiteto gyvenimą, infrastruktūrą, Lietuvos 
kultūrą, studentų galimybes bei teises. Atsižvelgiant į pandeminę situaciją, 
metų eigoje studentams buvo teikiama visa būtina ir aktuali informacija 
apie COVID-19 viruso plitimo valdymo kelius Lietuvoje, taikomus ribojimus, 
saugumo priemones. Kartu studentams buvo suteikta ir kontaktinė informacija 
užsikrėtimo liga atveju. Be visų standartinių renginių (protmūšių, 
ekskursijų, susipažinimo vakarų), metų eigoje buvo organizuoti 
<i>ESN Social Inclussion Days</i> renginiai – gestų kalbos mokymai ir –
bendradarbiaujant su VU SA programa Be Etikečių – paskaita apie rasizmą.  
Sveikatingumo savaitės metu rengtos žingsnių kiekio varžybos, paskaitos 
apie streso valdymą ir miesto pažinimo aktyviaisiais būdais iššūkiai. 
Pavasario semestro metu atgaivintas tarptautinis projektas <i>Erasmus in Schools</i>, 
kurio metu, bendraujant su VU užsienio studentais, mokiniai skatinami įsitraukti į mobilumą. 
Taip pat atgaivintas nacionalinis projektas <i>Multilingual Lithuania</i>, suteikiantis galimybę 
vietiniams studentams išmokti užsienio kalbos bendraujant su VU užsienio studentais kaip mokytojais. 
</p>
<p><i>ESN Vilnius University</i> per šiuos metus sustiprino ryšius su kitomis ESN Lithuania sekcijomis. Programa dalyvavo VU SA metiniuose planavimuose, kurių metu buvo išgrynintos dvi probleminės bylos, o jų įgyvendinimas – dalinai pasiektas. Dalyvauta įvairiuose VU SA mokymuose, kurių metų buvo gautos reikiamos kompetencijos. Vasario mėnesį ESN Vilnius University prisidėjo prie VU Tarptautinių ryšių skyriaus vietinių studentų mobilumo skatinimo konferencijų.</p> 
