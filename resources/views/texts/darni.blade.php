<h3 style="text-align: left;"><i><strong>VšĮ Vilniaus universiteto Teisės klinika (VU TK) </strong></i></h3>
<p><i>Ataskaitiniu laikotarpiu VU TK vadovavo Laurynas Totoraitis (iki 2020 m. rugpjūčio mėn.) ir Viktorija Medžiaušaitė (nuo 2020 m. rugsėjo mėn.).  
</i></p>
<p>VU SA ir partnerių įsteigta VšĮ <i>Vilniaus universiteto Teisės klinika</i> – 
nemokamos teisinės pagalbos projektas, kuris suteikia tikros profesinės praktikos 
galimybę vyresniųjų kursų teisės studentams, o visuomenei siūlo nemokamą teisinę 
pagalbą ir skatina teisinį raštingumą. Prieš 23 metus <i>VU TK</i> įsteigė ir iki šiol 
ją globoja VU SA, Vilniaus universitetas ir advokatų kontora <i>Ellex Valiunas</i>. </p>
<p>
Per metus teisinę pagalbą organizacija suteikė 4791 gyventojui. Remiantis 
interesantų atgaliniu ryšiu apie organizacijos veiklos kokybę galima daryti 
išvadą, kad <i>VU Teisės klinika</i> užtikrina pakankamai aukštą kompetencijos lygį 
ir gerą aptarnavimo kultūrą. </p>
</p>
<p>2020 m. veikla sėkmingai pritaikyta darbui karantino sąlygomis. 
Buvo vykdomos konsultacijos telefonu, <i>Skype</i> platformoje bei elektroniniu paštu. 
Vasaros laikotarpiu, laikantis visų apsaugos priemonių, teiktos kontaktinės konsultacijos. 
</p>
<p>Ataskaitiniu laikotarpiu <i>VU TK</i> privalomąją profesinę arba savanorišką 
praktiką atliko 28 Vilniaus universiteto studentai, kurios bendra trukmė yra 
6 mėnesiai. Praktikos metu studentai konsultavo interesantus šeimos, 
paveldėjimo, darbo, intelektinės nuosavybės, sutarčių, nekilnojamojo turto, vartotojų, 
administracinės, baudžiamosios teisės klausimais. Taip pat rengė sutarčių projektus, 
procesinių dokumentų projektus teisminėms institucijos (teismams, darbo ginčų komisijoms), 
teisines išvadas. 
</p>
<p>
<i>Vilniaus universiteto Teisės klinika</i> aktyviai bendradarbiavo su visuomenės 
informavimo priemonėmis, reguliariai teikė teisinius komentarus populiariausiems 
naujienų portalams <i>lrt.lt, 15min.lt, tv3.lt, delfi.lt, lrytas.lt,</i> radijo laidose 
(<i>Radijo byla</i>, 15/15). Tokiu būdu buvo didinamas visuomenės teisinio švietimo lygis. 
<i>Vilniaus universiteto Teisės klinikos</i> pavadinimas visuomenės informavimo priemonėse 
buvo paminėtas apie 40 kartų. 
</p>
