<p style="font-size: 50px; text-align: center;"><b>
Bendruomenę buriantys renginiai</b></p>
<p>Bendruomeniškumo puoselėjimas bei studentų integracija išlieka svarbiu Vilniaus universiteto Studentų atstovybės veiklos prioritetu. Jau kelerius metus iš eilės VU SA aktyviai rūpinasi socialinių–akademinių ir bendruomeninių renginių pasiūla, stiprina ir plečia projektus, klubus ir programas, kurie yra atviri visiems VU bendruomenės nariams. Taip VU SA skatina darnią, atvirą ir universitetines tradicijas puoselėjančią bendruomenę. </p>
<h3 style="text-align: center;"><STRONG>VU SA 31</STRONG></h3>
<p>Kiek kitaip nei kiekvienais metais, 
lapkričio 20-21 dienomis, buvo minima Tarptautinė studentų diena ir 
Vilniaus universiteto Studentų atstovybės 31-asis gimtadienis (toliau VU SA 31). 
Šių dviejų švenčių proga vyko trys diskusijos. Tarptautinę studentų dieną ir VU SA 31 
lapkričio 20 d. atidarė diskusija 
<a class="fs-6" href="https://youtu.be/rNX9kw_2rR0">„Kaip stiprinsime aukštąjį mokslą ateinančius ketverius metus?“</a>
, kurios metu buvo diskutuojama apie aukštąjį mokslą. Minėjimas tęsėsi lapkričio 21 d. 
kitomis dvejomis diskusijomis, kurios vyko anglų kalba. 
<a class="fs-6" href="https://youtu.be/TvuDCZccCzw">„COVID-19: students' movement without crossing borders“</a>
 Diskusijos metu buvo kalbama apie sunkumus, iššūkius 
bei problemas su kuriomis susiduria studentija pandemijos akivaizdoje norėdami 
išbandyti studijas svetur. Tarptautinės studentų dienos minėjimas buvo uždarytas 
diskusija 
<a class="fs-6" href="https://youtu.be/XblJYYnpc5Q">„What kind of student movement do students need today?“</a>
, kurios metu 
buvo diskutuojama apie tai, kokio studentų judėjimo reikia šių dienų studentijai. 
Nors negalėjome būti kartu, bet turėjome galimybę išgirsti įvairių žmonių mintis, 
pasisakymus studentams rūpimai klausimais. </p>
<h3 style="text-align: center;"><STRONG>VU Studijų mugė 2021 </STRONG></h3>
<p>Kaip ir dauguma renginių, taip ir kasmet vyksianti Studijų mugė šiais metais persikėlė į nuotolinę erdvę. Vilniaus universitetas savarankiškai ėmėsi iniciatyvos ir abiturientams suteikė kiek įmanoma panašesnės, į gyvai vykusios Studijų mugės, patirties. VU SA Studijų mugės metu buvo atsakinga ne tik už diskusijos su studentais apie studijas organizavimu, bet prisidėjo ir prie techninio renginio įgyvendinimo: studentai administravo nuotolinius konsultacijų kambarius ir net 2 savaites, nuo kovo 24 d. iki balandžio 2 d., visi stengėsi, jog Studijų mugė vyktų sklandžiai. </p>
<h3 style="text-align: center;"><STRONG>Padalinių dienos </STRONG></h3>
<p>2021 metais, kaip niekada, buvo daugybė kokybiškai organizuotų ir daug dalyvių pritraukusių padalinių dienų. Organizuoti padalinio dienas nuotoliniu būdu nė vienam padaliniui nebuvo problema, o visuose renginiuose netrūko originalių ir įtraukiančių veiklų. Šiais metais VU SA TF mus džiugino Konstitucijos bei Teisininkų dienomis, VU SA FF paminėjo jau 53-ias Fizikų dienas, 42-osios Istorikų dienos buvo puiki VU SA IF renginių maratono pradžia, Medikų dienose VU SA MF šiais metais mums suteikė galimybę išgirsti tikrų profesionalų paskaitų ir diskusijų, VU SA KF paruošė daug informacijos, kurią galėjome išgirsti per Informacijos dienas, VU SA FilF įkvėpė kūrybiškumo suorganizavę Filologų naktis, Verslo dienos po metų pertraukos sugrįžo ir pas VU SA VM, VU SA EVAF stebino suorganizavę puikias Merkurijaus dienas, garsiai nuskambėjusios VU SA MIF Matematikų ir informatikų dienos, o VU SA TSPMI Politikų dienos įtraukė tikrai ne tik politika besidominčius. Visos šios padalinių dienos, tai labai svarbus Studentų atstovybės indėlis į tradicijų puoselėjimą ir išsaugojimą. </p>
<h1 style="text-align: center;"><strong>Studentų integracija</strong></h1>
<h3 style="text-align: center;"><STRONG>VU pirmakursių stovyklos </STRONG></h3>
<p>2020 m., rugsėjo 8-14 dienomis, prieš prasidedant naujiems mokslo metams, Vilniaus universitete tradiciškai įvyko 16 VU SA organizuojamų pirmakursių stovyklų. Dėl Lietuvoje susiklosčiusios epidemiologinės situacijos šiemet buvo atsisakyta tradicinių, kelių dienų išvažiuojamųjų stovyklų – stovyklos sutrumpintos iki vienos dienos ir vyko kiemeliuose prie padalinių. Šiemet jose dalyvavo daugiau nei 1200 būsimų VU studentų. Stovyklų metu buvo susipažįstama su Universitetine kultūra, dėstytojais, alumnais bei padalinių administracijomis, taip pat pristatytas Vilniaus universitetas, suteikta bazinė informacija apie studijų procesą bei studijų aplinką, pristatytos VU SA veiklos sritys, programos, klubai ir projektai bei studentų galimybės VU. </p>
<h3 style="text-align: center;"><STRONG>Integracijos mugė </STRONG></h3>
<p> Nors šiais metais tradicine tapusi Rugsėjo 1-osios eisena nevyko, tačiau rugsėjo 12 d., mokslo metų pradžios proga, VU SA surengė dvylikos diskusijų su akademine bendruomene festivalį „Ad astra“. Visą dieną Vilniaus universiteto Centrinių rūmų kiemeliai skambėjo nuo diskusijų tarp skirtingų mokslo šakų atstovų, administracijos darbuotojų, studentų bei svečių. Diskutuota ne tik apie studijas, mokslą ir galimybes Universitete, bet ir apie informacinį pasaulį, propagandą, lygybę ir Universiteto lyderystę šalyje. 

Po diskusijų dalyvių laukė Universitete veikiančių organizacijų, programų, klubų, projektų mugė, kurioje studentai galėjo susipažinti su įvairiomis studentiškomis organizacijomis ir saviraiškos galimybėmis VU. Vakarą vainikavo Rektoriaus Rimvydo Petrausko bei VU SA prezidento Justo Kvedaravičiaus sveikinimo žodžiai ir bendruomenės koncertas Didžiajame kieme.  </p>

<h3 style="text-align: center;"><STRONG>VU 442 gimtadienis </STRONG></h3>
<p>Šiais metais Vilniaus universiteto gimtadienį turėjome švęsti nuotoliniu būdu. Kadangi jau ilgą laikotarpį visa VU bendruomenė skaudžiai išgyvena karantiną, buvo nuspręsta suorganizuoti šventę ir suburti visą Vilniaus universiteto bendruomenę. Daugiau nei 40 komandų išbandė save VU SA organizuojamame VU 442 gimtadienio protmūšyje „Socialinių tinklų pinklėse“. Labai džiaugiamės, kad ne ir tokiu sunkiu laikotarpiu Vilniaus universiteto bendruomenei sugebėjome suteikti dar vieną gražų, smagų ir bendruomenę buriantį renginį. </p>

