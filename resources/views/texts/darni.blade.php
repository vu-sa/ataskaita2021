<h3 style="text-align: left;"><i><strong>Vilniaus universiteto Studentų investicinis fondas (VU SIF)</strong></i></h3>
<p><i>Ataskaitiniu laikotarpiu fondui vadovavo Tautvydas Stasiulis (iki 2020 m. sausio mėn.) bei Ieva Turskytė (nuo 2020 m. sausio mėn.).  </i></p>
<p>
<i>Vilniaus universiteto Studentų investicinis fondas (VU SIF)</i> – VU SA įsteigtas pirmasis ir didžiausias studentų investicinis fondas Lietuvoje, skatinantis jaunimą domėtis investicijų valdymo pasauliu ir savarankiškai investuoti. 
</p>
<p>
Ataskaitiniu laikotarpiu <i>VU SIF</i> sėkmingai organizavo informatyvius renginius. 2020 m. birželio mėn. 
vyko virtualus pokalbis apie ekonomikos ir finansų tendencijas su tuometiniu Lietuvos finansų ministru 
Viliumi Šapoka, rugsėjo mėn. buvo paskelbtas tinklalaidės  epizodas su VU ekonomiste doc. dr. 
Ieva Bužiene, kurio metu pašnekovė dalijosi mintimis apie finansinį raštingumą. Studentai taip 
pat galėjo dalyvauti <i>VU SIF</i> renginiuose <i>Tautvydo Marčiulaičio paskaita: Didžiausi investicinio 
portfelio valdymo mitai</i> bei paskaitoje su Greta Krušaite <i>Asmeniniai finansai: kaupti, išleisti 
ar investuoti? VU SIF </i>bendradarbiavo su kitomis studentiškomis organizacijomis ir drauge su<i>  
ELSA Lithuania </i>kvietė dalyvauti atviroje diskusijoje <i>Akcijų rinkos manipuliacija: Kur brėžiama riba?</i> </p>
<p>
Spalį organizacija oficialiai tapo tarptautinio tinklo <i>IFSA Network</i>, vienijančio viso pasaulio studentus, 
besidominčius finansais ir investavimu, dalimi. Drauge su minėtuoju tinklu buvo organizuotas renginys 
apie investavimą su prelegentu Karoliu Kybartu.
</p>
<p>
Šie metai <i>VU SIF</i> buvo sėkmingi  konkursuose – komanda, 
kurią sudarė organizacijos nariai, užėmė pirmąją vietą 
tarptautiniame konkurse <i>Red Bull Basement</i>. Taip pat, 
prestižiniame konkurse <i>Global Case Competition at Harvard</i>, 
rengiamame pasaulio mastu organizacijos atstovai pateko į geriausiųjų 
komandų dvidešimtuką. Konkurso metu studentai iš geriausių pasaulio 
universitetų tris savaites sprendė konkretaus verslo atvejo problemą. 
</p>
<p>
<i>VU SIF</i> augti ir tobulėti ataskaitiniu laikotarpiu padėjo žurnalas <i>Investuok</i>, 
Lietuvos Bankas, UAB <i>Gerovės valdymas</i>, Vilniaus universiteto Verslo mokykla 
bei Vilniaus universitetas. 
</p>
