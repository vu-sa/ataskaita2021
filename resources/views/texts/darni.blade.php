<h3 style="text-align: left;"><i><strong>VU Kendo klubas (VU KK) </strong></i></h3>
<p><i>Ataskaitiniu laikotarpiu klubo veiklą koordinavo Vytenis Almonaitis.  
</i></p>
<p><i>Vilniaus universiteto Kendo klubas </i>– nuo 2008 m. veikianti VU SA japoniško fechtavimo studija, 
skirta tiek pradedantiesiems, tiek norintiems tobulinti jau turimus įgūdžius. <i>VU Kendo klubas</i> siekia ne tik populiarinti kendo sportą ir skatinti aktyvią gyvenseną, bet ir burti japoniškais kovos menais besidominčius studentus, garsinti Vilniaus universiteto vardą Lietuvoje ir pasaulyje, dalyvaudamas varžybose ir kituose renginiuose. 
</p>
<p>
Ataskaitiniu laikotarpiu klubas dalyvavo pirmakursiams skirtoje VU Karjeros dienų ašimi esančioje kontaktų mugėje. Dėl susiklosčiusios pandeminės situacijos šalyje, klubas, laikydamasis saugumo reikalavimų, negalėjo vykdyti treniruočių bei dalyvauti varžybose. Nepaisant to, studijos atstovai socialinėse medijose sekėjams pristatė solinių ir porinių treniruočių galimybes bei kvietė visus sportuoti. 
</p>
