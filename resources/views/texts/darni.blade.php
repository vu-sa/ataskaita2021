<h3 style="text-align: left;"><i><strong>Studentai – moksleiviams, moksleiviai – studentams (SMMS) </strong></i></h3>
<p><i>Ataskaitiniu laikotarpiu projekto veiklą koordinavo Aurelija Vasiliauskaitė. 
</i></p>
<p>
<i>SMMS (Studentai – moksleiviams, moksleiviai – studentams)</i> – VU SA vykdomas projektas, skirtas vyresnių klasių moksleiviams, norintiems iš arčiau susipažinti su studentišku gyvenimu ir studijų galimybėmis.
</p>
<p>
Šiemet projekto veikla buvo vykdoma nuotoliniu būdu. Dar lapkričio mėnesį <i>START FM</i> studijoje 
suorganizuotas pokalbis apie <i>SMMS</i> su Ieva ir Mantu Bartuševičiais. Sausio – vasario mėnesiais 
projekto kuratoriai vedė klasės valandėles vyresnių klasių moksleiviams, kur buvo  pristatytas 
<i>SMMS</i> projektas, Vilniaus universitetas, VU ir VU SA popaskaitinių veiklų galimybės bei atsakinėta 
į moksleiviams kylančius klausimus. Nuo vasario mėnesio pradžios <i>SMMS</i> socialiniuose tinkluose 
pradėti VU padalinių pristatymai, kurių metu, kas savaitę, <i>SMMS</i> kuratoriai pristatė vis kitą VU padalinį. 
Gegužės mėn. 15 d. vyks nuotolinė <i>SMMS</i> stovykla, o po jos moksleiviai turės galimybę užduoti 
rūpimus klausimus <i>SMMS</i> kuratoriams <i>Discord</i> platformoje. 
</p>
