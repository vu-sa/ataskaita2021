<h3 style="text-align: left;"><i><strong>Labdaros ir paramos fondas Ateičiai </strong></i></h3>
<p><i>VU SA atstovas dalininkų susirinkimuose – Justas Kvedaravičius. </i></p>
<p>
Vilniaus universiteto Studentų atstovybė 2018 m. įkūrė labdaros ir paramos fondą <i>Ateičiai</i>, skirtą Vilniaus universiteto studentų gerovei kurti. Fondas buvo įkurtas siekiant atstovauti įvairius studentų poreikius. 
</p>
<p>
Labdaros ir paramos fondas <i>Ateičiai</i> ataskaitiniu laikotarpiu viešino ir garsino Vilniaus universiteto vardą platindamas atributiką tiek fizinėje, tiek elektroninėje parduotuvėse. Šiais metais bendradarbiaujant su Klaipėdos universiteto Studentų sąjunga buvo atidaryta elektroninė Klaipėdos universiteto atributikos parduotuvė. Taip pat, fondas plečia bendradarbiavimą su VU – pasirašyta bendradarbiavimo sutartis su VU Kultūros centru dėl jų išleistų kompaktinių diskų platinimo. Šiais metais pradėta iniciatyva – renkama 1,2 % GPM parama, kuri bus tikslingai naudojama stipendijoms, skirtoms socialiai remtiniems Vilniaus universiteto studentams už jų vykdomą visuomeninę veiklą, įsteigti. 
</p>
<p>
Labdaros ir paramos fondo <i>Ateičiai</i> veikla yra socialiai atsakinga bei kurianti kapitalą Vilniaus universiteto studentų gerovės labui. </p>
</p>
