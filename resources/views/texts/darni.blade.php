<h3 style="text-align: left;"><i><strong>Sąžiningai </strong></i></h3>
<p><i>Ataskaitiniu laikotarpiu programos veiklą koordinavo Greta Čepurnaitė (iki 2020 m. spalio mėn.). </i></p>
<p>
<i>Sąžiningai</i> – VU SA programa, puoselėjanti Vilniaus universiteto bendruomenės akademinę etiką ir 
skatinanti akademinio sąžiningumo kultūrą. Prie programos veiklos prisidedantys dalyviai siekia 
mažinti nusirašinėjimo, plagijavimo ir rašto darbų pirkimų atvejus. Akademinio sąžiningumo 
skatinimui yra rengiami vykdomų tarpinių ir galutinių atsiskaitymų (kolokviumų ir egzaminų) 
stebėjimai, organizuojamos informacinės kampanijos, rubrikos, atliekami tyrimai, rengiami 
straipsniai ir apžvalgos. Programos atstovai veiklą vykdo visuose Vilniaus universiteto 
kamieniniuose akademiniuose padaliniuose. 
</p>
<p>
Ataskaitiniu laikotarpiu vyko programos <i>Sąžiningai</i> veiklos krypčių persvarstymas ir išgryninimas, 
siekiant padaryti ją labiau aktualią ir naudingą studentams. Diskusijų metu išsigrynintos 3 
programos veiklos kryptis: atsiskaitymų stebėjimas, studentų švietimas bei akademinės etikos 
kultūros diegimas Universitete. 
</p>


