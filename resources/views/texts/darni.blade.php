<h3 style="text-align: left;"><i><strong>VU radijas START FM </strong></i></h3>
<p><i>Ataskaitiniu laikotarpiu radijui vadovavo Rimantas Murza. 
</i></p>
<p>
VU radijas <i>START FM</i> – VU SA ir Vilniaus universiteto radijo stotis, 
veiklą pradėjusi 2005 metais. Vienintelė Lietuvoje studentų radijo 
stotis suteikia plačias saviraiškos galimybes studentams: padeda 
rengti būsimus žurnalistus, programuotojus ir kitų sričių specialistus. 
<i>START FM</i> taip pat aktyviai palaiko jaunų bei mažai žinomų Lietuvos ir 
užsienio atlikėjų ir muzikos grupių kūrybos sklaidą, rengia interviu 
su alternatyvios muzikos atlikėjais, palaiko įvairias menines bei socialines iniciatyvas. 
</p>
<p>
2020 m. rugsėjo mėn. 12 d. atšventusi 15-ąjį gimtadienį, Vilniaus Universiteto 
radijo stotis <i>START FM 94.2</i> net ir besitęsiančio karantino metu nesustoja 
augti. 2020 m. per savaitę buvo transliuojama vidutiniškai 48 valandos 
originalaus programavimo (kitą programos dalį sudaro automatinis muzikos grojimas), 
o internetu eterį įsijungdavo vidutiniškai 42 000 unikalių klausytojų per mėnesį. 
</p>
<p>
Augimas nesustojo ir bendradarbiavimo vystyme – pradėti bendri projektai su 
Lietuvos tarpdisciplinio meno kūrėjų sąjunga, šiuolaikinės rimtosios muzikos 
festivaliu <i>Druskomanija</i>, neformalia muzikos kūrybos mokykla <i>Muzikalkė</i>, radijo 
teatru <i>Bilietų Nėra</i> (studijoje įrašytas ir VU radijo eteryje transliuotas 
spektaklis <i>Šiaurės Miestelis</i>, Lietuvos radijo ir televizijos komisijos 
rengiamame konkurse <i>Pragiedruliai</i> pripažintas originaliausia radijo laida). 
Taip pat įvykdytas projektas <i>3 minutės solo</i>, kurio metu buvo profesionaliai 
nufilmuoti ir įrašyti 32 jaunieji Lietuvos džiazo muzikos instrumentalistai 
atliekantys 3 min. trukmės solo pasirodymą. Rudens pradžioje iš VU studentų 
suburta <i>START FM</i> žinių komanda kiekvieną darbo dieną rengia naujienų pranešimus, 
reportažus aktualiomis temomis bei interviu. </p>
