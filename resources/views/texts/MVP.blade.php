    <h1>VU SA 2020–2021 metų veiklos plano įgyvendinimas </h1>
    <p>
        VU SA 2020–2021 metų veiklos plane nurodomi pagrindiniai šių metų tikslai ir uždaviniai. 
        Planas yra skirtas numatyti VU SA veiklos prioritetus, 
        kuriais būtų kuriama vieninga ir atvira aplinka savarankiškam studento tobulėjimui Vilniaus universiteto 
        bendruomenėje, ir sistemingai pasiruošti šių veiklų įgyvendinimui.  
        VU SA metų veiklos planas yra sudarytas iš strateginių tikslų ir VU SA padalinių išsikeltų tikslų. 
        Strateginiai tikslai – tai tikslai, kuriais siekiama permainų viso Universiteto mastu. 
        Ši sąvoka apima VU SA inicijuojamus veiksmus, kuriais siekiama spręsti problemas, 
        aktualias visiems Vilniaus universiteto studentams. VU SA padalinių tikslai yra 
        skirti užtikrinti studentų socialinės ir akademinės aplinkos tobulinimą kiekviename 
        Vilniaus universiteto kamieniniame akademiniame padalinyje (toliau – KAP) ir yra 
        keliami atsižvelgiant į kiekvieno KAP studentų poreikius individualiai. 
        Tikslingai siekiant metų veiklos plane numatytų pokyčių, tikslų įgyvendinimas 
        tęsiasi ir po VU SA ataskaitinės-rinkiminės konferencijos. Taigi, šioje ataskaitoje 
        apžvelgiami metų veiklos plano vykdymo rezultatai nėra galutiniai ir veiklos bus vykdomos toliau.  
        Detaliau su metų veiklos planu ir planavimo proceso eiga susipažinkite:
        www.vusa.lt → Dokumentai → VU SA dokumentai → Veiklą reglamentuojantys dokumentai.  
        Kviečiame susipažinti su 2020–2021 metų veiklos plane išsikeltų tikslų rezultatais. 
    </p>

