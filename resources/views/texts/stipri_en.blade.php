    <h3 style="text-align: center;">VU SR Projects </h3>
    <p>During the reporting period, VU SR implemented 2 projects, the implementation of both projects is still ongoing. The applications of 2 projects were also submitted, both of which are still awaiting the final decision of the evaluation committee. </p>
    <p>Currently, The State Studies Foundation is expected to respond to the evaluation of the projects “Enabling Student Financial Initiatives through the Development of Financial Literacy Competences” and “Increasing Student Involvement in Fostering Academic Ethics in the University Community”. </p>
    <p>Together with the Knowledge Economy Forum, the Lithuanian Union of Young Scientists and the Šiauliai University Students’ Representation, a project of Ministry of the Interior was launched to strengthen the project partners' advocacy skills in the fields of education and its systematization, quality and scope of participation in public administration processes. During this year, 3 surveys of students covering different areas of their lives were prepared to achieve the project objectives. </p>
    <p>Funding has been received from the European Union Structural Funds for the ECSTRA project, which aims to promote students' self-expression, informal activities and competences acquired in an informal way. During this year, During the implementation of the project, a structure for the establishment of new student initiatives and a guide was prepared which will be addressed to all those in senior positions in the organization. <p>

    <h3 style="text-align: center;">INSTITUTIONAL STRENGTHENING </h2>
    <p>This year, 25 trainings were organized with about 750 participants. The main topics covered during the trainings were:  </p>
    <ul>
        <li>the strategy, structure and image of the organization; </li>
        <li>student counseling, provision of social and academic knowledge, representation, gathering of students' opinions; </li>
        <li>organization of unit days, preparation of estimates, cooperation between areas; </li>
        <li>teamwork remotely, communicating the importance of responsibility and refining positions. </li>
    </ul>
    <h3 style="text-align: center;">HUMAN RESOURCES MANAGEMENT </h3>
    <p>Systematic work with human resources continued this year: </p>
    <ul>
        <li>in summer, for the first time, joint scope planning was carried out according to the key issues identified during the summer Office-Board meeting; </li>
        <li>Regular meetings between the Institutional Strengthening Fund and the Human Resources Coordinators continued (6 in total), during which the coordinators presented projects on a monthly basis on the following topics:  
        <ul>
            <li>attracting and retaining members; </li>
            <li>time management; </li>
            <li>feedback; </li>
            <li>emotion management; </li>
            <li>transmission of experience; </li>
            <li>nformation of a new team. </li>
        </ul>
        </li>
        <li>competence development took place not only during trainings, but also during regular and general meetings, through simulations; </li>
        <li>after the Šiauliai Academy joined, the integration of VU SR ŠA into the organization was carried out, basic knowledge, planning, team formation were provided; </li>
        <li>the first joint trainings of programs, clubs, projects took place, in which they refined the importance of their presence in the organization, outlined the further course of action;</li>
        <li>more central trainings took place:   
