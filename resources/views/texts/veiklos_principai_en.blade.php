    <h2>About VU SA</h2>
        <p><strong>Vilnius University Students’ Representation (VU SR, lt. VU SA) </strong>
        is the oldest and the biggest expert education organization in Lithuania. It represents the interests of students at Vilnius University and beyond, fostering university culture and creating the most favourable conditions for students’ self-expression.  
        </p>
        <p>In total, VU SA has 15 units in each core academic unit (faculty, institute, or centre) that represent students locally. VU SA also has 15 programs, clubs, projects and public bodies (lt. PKP) as well as one Central Office, which coordinates all the units, PKP and Vilnius University Alumni Society. For this day VU SA unites around 1 000 members.  
        </p>
        <p><strong>VU SA:</strong></p>
        <ul>
            <li>represents students – defends students’ rights in University’s governing bodies and on the national level;  </li>
            <li>contributes to the improvement of the study process in the University, is a part of all the most important governing bodies of Vilnius University;    </li>
            <li>takes care of social welfare – provides a comfortable and student–friendly environment, takes care of student integration, participates in the distribution of scholarships;  </li>
            <li>organizes meaningful leisure time;  </li>
            <li>spreads the information so that all relevant news reaches the students. </li>
        </ul>
