    <p>
    VU SR 2020–2021 annual activity plan sets out the main goals and objectives for the year. The plan is intended to establish VU SR operational priorities that would create a unified and open environment for independent self-development of students in the Vilnius University community and systemically prepare for the implementation of these activities.  
    </p>
    <p>The VU SR annual activity plan consists of strategic goals and goals set by VU SR units. Strategic goals are aimed for change on the University scale. This concept encompasses actions initiated by VU SR, which aim to solve problems relevant to all students of Vilnius University. The goals of VU SR units are intended to ensure the improvement of students' social and academic environment in each core academic unit (hereinafter – CAU) of Vilnius University and are set taking into account the needs of students of each CAU individually. </p>
    <p>Purposefully striving for the changes set out in the annual activity plan, the implementation of the goals continues after the VU SR Convention. Therefore, the results of the implementation of the annual activity plan reviewed in this report are not final and the activities will be carried on.  </p>
    <p>For more details on the annual activity plan and the course of the planning process, see: 
www.vusa.lt → Documents → Operating documents. </p>
    <p>We invite you to get acquainted with the results of the goals set in the 2020-2021 annual activity plan. </p>

