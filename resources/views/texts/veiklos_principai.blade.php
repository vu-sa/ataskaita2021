            VU SA Parlamentas – VU SA valdymo organas, svarstantis svarbiausius socialinius ir akademinius, 
            su studentais susijusius ar kitus VU SA strateginius klausimus. Parlamentą šiais metais sudarė 46 asmenys – 
            3 daugiau po VU SA ŠA steigiamosios konferencijos. Parlamentą sudaro organizacijos prezidentas, atstovavimo funkciją 
            vykdančių VU SA padalinių pirmininkai ir po du atstovus iš kiekvieno padalinio. Parlamento darbą organizuoja pirmininkas. 
