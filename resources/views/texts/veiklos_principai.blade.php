            už kurios puoselėjimą yra atsakingas kiekvienas Vilniaus universiteto bendruomenės narys. Siekiant pagerinti Universiteto 
            bendruomenės emocinę būklę, ypač svarbu daugiau dėmesio skirti psichologinės pagalbos teikimui aukštojoje mokykloje. 
            Nors tam tikrų veiksmų Universitete imamasi, šiuo metu esamų priemonių nepakanka. Universitetas turi siekti visapusiškai 
            spręsti emocinės aplinkos klausimą.
        </p>
        <p>
            <strong>VU SA studentų darbo programa </strong></p>
        <p>
            Norėdama sudaryti sąlygas turėti galimybę nedirbti ir pagerinti aukštojo mokslo 
            bendruomenės materialines sąlygas VU SA: 
            <p><strong>1.1. </strong>vadovaujasi nuostata, kad studijų 
            proceso dalyviams turi būti suteiktos visos kokybiško studijų proceso įgyvendinimui 
            reikalingos priemonės ir infrastruktūra; </p>
            <p><strong>1.2. </strong>bendradarbiauja su didesnio aukštojo 
            mokslo ir studijų finansavimo siekiančiomis ir kitomis organizacijomis.</p>
        </p>

        <p>
            <strong>Rezoliucija dėl studentų darbo studijų metu </strong></p>
        <p>
            Universitete svarbiausia yra studijų ir mokslo kokybė. VU SA laikosi pamatinio 
            principo, kad nepaisant materialinių sąlygų studentas turi turėti galimybę visą 
            savo laiką paskirti tik studijoms, o papildomas veiklas rinktis laisva valia. 
            Reikšminga šalies studentų dalis galėtų daugiau laiko skirti studijoms ir mokslui, 
            jeigu dėl materialinių priežasčių nebūtų priversta dirbti, studentų darbas dėl 
            pragyvenimo turi neigiamos įtakos studijų kokybei, o dabar studentams suteikiamos 
            materialinės paramos nepakanka išspręsti šios problemos. Į tai atsižvelgdamas VU SA 
            Parlamentas priima principines nuostatas, kuriomis vadovaujantis turėtų būti 
            sprendžiama studentų darbo dėl materialinių priežasčių problema.
        </p>

        <p>
            <strong>Pozicija dėl pagalbos COVID-19 pandemijos metu nukentėjusiems studentams </strong></p>
        <p>
            VU SA Parlamentas atkreipia dėmesį į šalyje susidariusią ekstremalią situaciją, 
            kuri smarkiai paveikė Vilniaus universiteto bendruomenę. COVID-19 sukelta pandemija 
            universitetiniam gyvenimui turėjo ne tik tiesioginės (užsikrėtę virusu arba su 
            sergančiuoju turėję kontaktą privalo izoliuotis), bet ir netiesioginės įtakos 
            (artimojo netektis, atleidimas iš darbo ir kitos asmeninės problemos).
        </p>

        <p>
            <strong>Rezoliucija dėl lygių galimybių užtikrinimo ir įvairovės skatinimo Vilniaus universitete  </strong></p>
        <p>
            VU SA Parlamentas priima šią rezoliuciją, patvirtindamas principines nuostatas dėl lygių 
            galimybių užtikrinimo kiekvienam akademinės bendruomenės nariui ir narei Vilniaus universitete. 
            Geresnis socialiai jautrių grupių integravimas į aukštojo mokslo bendruomenę leidžia sumažinti 
            socialinę nelygybę ir teikia naudą ne tik pačiam individui, bet ir universiteto akademinei 
            bendruomenei bei visuomenei. Sudarant galimybes kiekvienam asmeniui išpildyti savo potencialą
             yra didinamas tarpusavio supratimas bei kuriama sveikesnė, pilietiškai aktyvesnė ir 
             talentingesnė visuomenė, prisidedanti prie valstybės tarptautinio konkurencingumo didinimo. 
             Dėl šių priežasčių Universitetas visuose socialinės dimensijos koncepcijoje išskirtuose lygmenyse 
             – (I) prieš stojant (angl. before entry); (II) stojimo metu (angl. at entry); (III) studijų metu; 
             (IV) pereinant į darbo rinką – privalo skirti daugiau dėmesio nereprezentuojamų grupių 
             poreikių įvertinimui ir lygių galimybių užtikrinimui.
        </p>

