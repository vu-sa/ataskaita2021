            darbo studijų metu, dėl lygių galimybių užtikrinimo ir įvairovės skatinimo Vilniaus universitete. Patvirtinta pozicija dėl 
            pagalbos COVID-19 pandemijos metu nukentėjusiems studentams. Viename iš Parlamento posėdžių apie 2020 metų aukštojo mokslo 
            apžvalgą diskutuota su LR švietimo, mokslo ir sporto viceministru Gintautu Jakštu. Kitame posėdyje apie Vilniaus universiteto 
